int	ft_atoi(char *str)
{
	long int	sum;
	int			sign;

	sum = 0;
	sign = 1;
	while (*str && (((*str >= 9) && (*str <= 13)) || *str == ' '))
	{
		str++;
	}
	while (*str && (*str == '-' || *str == '+'))
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str && (*str >= '0' && *str <= '9'))
	{
		sum *= 10;
		sum += *str - '0';
		str++;
	}
	return (sum * sign);
}
