#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len])
		len++;
	return (len);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = 0;
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*ret;
	int		len;

	len = ft_strlen(src);
	ret = (char *) malloc((len + 1) * sizeof(char));
	if (ret != NULL)
		ft_strcpy(ret, src);
	return (ret);
}
